    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        try {
            // TODO add your handling code here:
            String truePass = DatabaseOperations.getUserPass(username.getText());
            String enteredPass = "";
            for(char c : password.getPassword()){
                enteredPass+=c;
            }
            if(truePass.equals(enteredPass)){
                MainScreen ms = new MainScreen();
                this.setVisible(false);
                this.dispose();
                ms.setProfile(DatabaseOperations.getProfileByUsername(username.getText()),isConnected);
                ms.setVisible(true);
                
            }
            else{
                loginInfo.setForeground(Color.red);
                loginInfo.setText("Check your login information");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginMouseClicked

    private void passwordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
         try {
            // TODO add your handling code here:
            String truePass = DatabaseOperations.getUserPass(username.getText());
            String enteredPass = "";
            for(char c : password.getPassword()){
                enteredPass+=c;
            }
            if(truePass.equals(enteredPass)){
                MainScreen ms = new MainScreen();
                this.setVisible(false);
                this.dispose();
                ms.setProfile(DatabaseOperations.getProfileByUsername(username.getText()),isConnected);
                ms.startsChats();
                ms.setVisible(true);
                
            }
            else{
                loginInfo.setForeground(Color.red);
                loginInfo.setText("Check your login information");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }   
        }
    }//GEN-LAST:event_passwordKeyPressed

